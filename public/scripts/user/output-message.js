// output error message
function outputErrorMessage(msg) {
  $.notify(msg, 'error');
}

// output success message
function outputSuccessMessage(msg) {
  $.notify(msg, 'success');
}

// output warning message
function outputWarnMessage(msg) {
  $.notify(msg, 'warn');
}

// output info message
function outputInfoMessage(msg) {
  $.notify(msg, 'info');
}
